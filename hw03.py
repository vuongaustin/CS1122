def q5 (n): # Convert octal to decimal
    n_string = str(n)
    dec_num = 0
    counter = 0
    for i in n_string:
        counter += 1
        i_int = int(i)
        dec_num += i_int * (8 ** (len(n_string) - counter))
    print (dec_num)

